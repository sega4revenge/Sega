package com.github.javiersantos.materialstyleddialogs.enums;

/**
 * Duration for the dialog animations.
 *
 * <li>{@link #NORMAL}</li>
 * <li>{@link #FAST}</li>
 * <li>{@link #SLOW}</li>
 */
public enum Duration {
    /**
     * Normal, 300 milliseconds.
     */
    NORMAL,

    /**
     * Fast, 150 milliseconds.
     */
    FAST,

    /**
     * Slow, 400 milliseconds.
     */
    SLOW
}
